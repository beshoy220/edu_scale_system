CREATE TABLE schools (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
    school_domain VARCHAR(50) UNIQUE NOT NULL, -- same as email domain
    name VARCHAR(100) NOT NULL,
    address VARCHAR(255) NOT NULL,
    logo_url TEXT,
    school_weekend VARCHAR(50)[] DEFAULT ARRAY[]::VARCHAR(50)[] NOT NULL, -- EX: ['Sat', 'Fri']
    educational_system VARCHAR(36) CHECK (educational_system IN ('Private National','American', 'IGCSE', 'IB', 'Other')) NOT NULL,
    class_max_capacity INT DEFAULT 25, 
    status VARCHAR(36) DEFAULT 'suspended' CHECK (status IN ('active','suspended','deleted')), -- suspended (means not set up yet), deleted (means the school was a client but they left forever)
    created_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE school_plans (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    description VARCHAR(255) NOT NULL,
    price_per_user INT NOT NULL,
    created_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE school_subscriptions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
    school_id BIGINT NOT NULL,
    plan_id BIGINT NOT NULL,
    users_limit INT NOT NULL, -- The number of users school purchased
    library_limit_in_gb INT NOT NULL, -- The total size of the files the school pushased in GB
    start_at DATE NOT NULL,
    end_at DATE NOT NULL,
    created_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (school_id) REFERENCES schools(id),
    FOREIGN KEY (plan_id) REFERENCES school_plans(id)
);



-- ==========================================================
-- SCHOOL STRUCTURE LAYER - [SYSTEM]
-- ==========================================================

CREATE TABLE grades (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
    school_id BIGINT NOT NULL,
    name VARCHAR(50) CHECK (name IN ('Grade 1', 'Grade 2', 'Grade 3', 'Grade 4', 'Grade 5', 'Grade 6', 'Grade 7', 'Grade 8', 'Grade 9', 'Grade 10', 'Grade 11', 'Grade 12')) NOT NULL,
    created_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (school_id) REFERENCES schools(id)
);

CREATE TABLE classes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
    school_id BIGINT NOT NULL,
    grade_id BIGINT NOT NULL,
    name VARCHAR(50) CHECK (name IN ('Class A', 'Class B', 'Class C', 'Class D', 'Class E', 'Class F', 'Class G', 'Class H', 'Class I', 'Class J')) NOT NULL, 
    nickname VARCHAR(50) NOT NULL, -- we usually set it same as name for first time
    max_capacity INT DEFAULT 25,
    created_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (school_id) REFERENCES schools(id),
    FOREIGN KEY (grade_id) REFERENCES grades(id)
);


CREATE TABLE subjects (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
    school_id BIGINT NOT NULL,
    name VARCHAR(255) NOT NULL, -- Too many subjects can found so we can not add CHECK constraint here.
    created_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (school_id) REFERENCES schools(id)
);



-- ==========================================================
-- USERS LAYER - [SYSTEM]
-- ==========================================================

CREATE TABLE users (
    id UUID UNIQUE PRIMARY KEY NOT NULL, -- same uuid in auth.users table 
    school_id BIGINT NOT NULL,
    role VARCHAR(20) CHECK (role IN ('admin', 'teacher', 'student', 'parent')) NOT NULL, -- this is not for authorization, its just for filtering users and targting them in easy way 
    name VARCHAR(100) NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL,
    avatar_url TEXT, -- avatars are png files hosted in github to reduces app size
    phone VARCHAR(50),
    status VARCHAR(50) DEFAULT 'pending' CHECK (status IN ('active', 'pending', 'suspended')) NOT NULL, -- active, pending (invited but not registered yet), suspended (suspended by school admin or edu-scale team)
    gender VARCHAR(20) CHECK (gender IN ('male', 'female')),
    birthday DATE,
    fcm_token TEXT,
    -- national_id VARCHAR(50),
    created_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (school_id) REFERENCES schools(id)
);

CREATE TABLE user_profiles (
    user_id UUID PRIMARY KEY NOT NULL, -- same uuid in auth.users table and public.users table
    grade_id BIGINT,
    class_id BIGINT,
    subject_id BIGINT,
    parent_id UUID,
    student_id UUID,
    extra JSON,
    FOREIGN KEY (user_id) REFERENCES users(id),
    FOREIGN KEY (grade_id) REFERENCES grades(id),
    FOREIGN KEY (class_id) REFERENCES classes(id),
    FOREIGN KEY (subject_id) REFERENCES subjects(id),
    FOREIGN KEY (parent_id) REFERENCES users(id),
    FOREIGN KEY (student_id) REFERENCES users(id)
);



