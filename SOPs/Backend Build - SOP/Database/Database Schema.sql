CREATE TABLE mr_everything_chats (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
    school_id BIGINT NOT NULL,
    user_id UUID NOT NULL,
    title VARCHAR(255) NOT NULL, -- auto-generated title by ai
    created_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES users(id),
    FOREIGN KEY (school_id) REFERENCES schools(id)
);

CREATE TABLE mr_everything_chat_messages (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
    chat_id BIGINT NOT NULL,
    user_id UUID NOT NULL,
    sender VARCHAR(12) CHECK (sender IN ('user', 'ai')) NOT NULL,
    message_text TEXT NOT NULL,
    assignment_questions_id BIGINT, -- if the message is sent with an assignment question, it will be stored here, otherwise it will be null
    quiz_questions_id BIGINT, -- if the message is sent with a quiz question, it will be stored here, otherwise it will be null
    -- library_id BIGINT, -- WE MAY ADD IT (We should study that option first)
    -- competition_id BIGINT, -- WE MAY ADD IT (We should study that option first))
    -- time_table_id BIGINT, -- WE MAY ADD IT (We should study that option first))
    -- event_id BIGINT, -- WE MAY ADD IT (We should study that option first)
    created_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (chat_id) REFERENCES mr_everything_chats(id),
    FOREIGN KEY (user_id) REFERENCES users(id),
    FOREIGN KEY (assignment_questions_id) REFERENCES assignment_questions(id),
    FOREIGN KEY (quiz_questions_id) REFERENCES quiz_questions(id)
);



