-- ==========================================================
-- ASSIGNMENTS - [FEATURE]
-- ==========================================================

CREATE TABLE assignments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
    school_id BIGINT NOT NULL,
    teacher_id UUID NOT NULL,
    subject_id BIGINT NOT NULL,
    grade_id BIGINT NOT NULL,
    class_id BIGINT, -- if null, it means the assignment is for the whole grade
    topic VARCHAR(255) NOT NULL,
    publish_status VARCHAR(50) CHECK (publish_status IN ('published', 'unpublished')) NOT NULL,
    number_of_questions INT CHECK (number_of_questions BETWEEN 1 AND 20) NOT NULL,
    due_date TIMESTAMPTZ NOT NULL, -- if due date passed then the assignment is past assignment and students can not submit anymore
    created_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (school_id) REFERENCES schools(id),
    FOREIGN KEY (teacher_id) REFERENCES users(id),
    FOREIGN KEY (subject_id) REFERENCES subjects(id),
    FOREIGN KEY (grade_id) REFERENCES grades(id),
    FOREIGN KEY (class_id) REFERENCES classes(id)
);

CREATE TABLE assignment_questions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
    school_id BIGINT NOT NULL,
    assignment_id BIGINT NOT NULL,
    question_order INT NOT NULL, -- the order of the question in the assignment, starts from 1 to number_of_questions
    question_text TEXT NOT NULL,
    options_1 VARCHAR(255) NOT NULL,
    options_2 VARCHAR(255) NOT NULL,
    options_3 VARCHAR(255),
    options_4 VARCHAR(255),
    options_5 VARCHAR(255),
    options_6 VARCHAR(255),
    model_answer VARCHAR(255) NOT NULL,
    FOREIGN KEY (school_id) REFERENCES schools(id),
    FOREIGN KEY (assignment_id) REFERENCES assignments(id)
);


CREATE TABLE assignment_student_submissions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
    school_id BIGINT NOT NULL,
    student_id UUID NOT NULL,
    assignment_id BIGINT NOT NULL,
    number_of_correct_questions INT DEFAULT 0 NOT NULL,
    total_number_of_questions INT DEFAULT 0 NOT NULL,
    created_at TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (school_id) REFERENCES schools(id),
    FOREIGN KEY (assignment_id) REFERENCES assignments(id),
    FOREIGN KEY (student_id) REFERENCES users(id)
);

CREATE TABLE assignment_student_answers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
    school_id BIGINT NOT NULL,
    submission_id BIGINT NOT NULL,
    assignment_question_id BIGINT NOT NULL,
    answer VARCHAR(255) NOT NULL, -- student answer
    is_correct BOOLEAN DEFAULT FALSE NOT NULL,
    FOREIGN KEY (school_id) REFERENCES schools(id),
    FOREIGN KEY (submission_id) REFERENCES assignment_student_submissions(id),
    FOREIGN KEY (assignment_question_id) REFERENCES assignment_questions(id)
